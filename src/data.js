/**
 * Конвертация lat/lng в 3D-позицию на сфере
 */
export function latLngToVector3(lat, lng, radius = 1) {
  const phi   = (90 - lat) * (Math.PI / 180);
  const theta = (lng + 180) * (Math.PI / 180);

  return {
    x: -(radius * Math.sin(phi) * Math.cos(theta)),
    y:  (radius * Math.cos(phi)),
    z:  (radius * Math.sin(phi) * Math.sin(theta)),
  };
}

/**
 * Генерация ~2000 фейковых следов
 */
export function generateTraces() {
  const traces = [];
  const now = Date.now();
  const THIRTY_DAYS = 30 * 24 * 60 * 60 * 1000;

  for (const [lat, lng, country, weight] of REGIONS) {
    for (let i = 0; i < weight; i++) {
      const cat = CATEGORIES[Math.floor(Math.random() * CATEGORIES.length)];
      const textsForCat = TEXTS[cat.id];
      const text = textsForCat[Math.floor(Math.random() * textsForCat.length)];

      // Разброс координат вокруг центра региона (±3°)
      const jitterLat = lat + (Math.random() - 0.5) * 12;
      const jitterLng = lng + (Math.random() - 0.5) * 12;

      traces.push({
        id: traces.length,
        text,
        category: cat.id,
        categoryLabel: cat.label,
        color: cat.color,
        country,
        lat: jitterLat,
        lng: jitterLng,
        date: new Date(now - Math.random() * THIRTY_DAYS),
      });
    }
  }

  return traces;
}
